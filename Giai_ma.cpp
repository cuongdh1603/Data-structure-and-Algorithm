#define ll long long
using namespace std;
vector<int> num;
bool check(string s){
    if(s[0]=='0') return false;
    for(int i=s.size()-1;i>0;i--){
        if(s[i]=='0'&&(s[i-1]=='0'||s[i-1]>'2'))
            return false;
    }
    return true;
}
int solve(int n){
    vector<ll> dp;
    dp.resize(n+1,0);
    dp[0] = dp[1] = 1;
    for(int i=2;i<=n;i++){
        if(num[i-1]) dp[i] = dp[i-1];
        if(num[i-2]==1||(num[i-2]==2&&num[i-1]<7)) dp[i] += dp[i-2];
    }
    return dp[n];
}
